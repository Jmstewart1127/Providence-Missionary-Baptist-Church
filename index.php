					<h3>History of PMBC</h3>
                    <p>In a move from his native homeland of Arkansas to Missouri, our illustrious founder and first pastor Robert Burton Porter reflects that the cause of his move was the “Providence of God.” And God in His infinite wisdom gave us the name of our newfound church in 1926.</p>
                    <p>The seed was planted in 1926 when a prayer band held a meeting at the home of brother and Sister Will and Anna Scaggs. The seeds were from several homes, those of Sister and Brother Cora Precious Hale, Brother Jessie Precious, and Joe and Lizzle Gary, Emma Chalk, James Lawson and Bee Smith. This band later moved to 9th and Euclid, where the prayer band was duly organized Providence Missionary Baptist Church, under the esteemed leadership of Dr. D.A. Homes, Moderator, and other ministers of the city including the Reverends N.T. Lane, James Kelly, T.M. Montgomery, J.H. Patton, S. McNanamay and R.B. Porter who later was called to be the pastor of the church. Some of the early members were sis. Ella Jennings Torrence, Andie Rollings, Annie Smith and Brother and Sister Duchie Spike.</p>
                    <p>Under God’s watchful eye and the work of the innovative seedlings (our pioneers) Providence flourished. Beginning the pastoral period of Pastor Robert Burton Porter, the church moved from the 9th St. location to the church building 12th and Brooklyn. The church was named Providence Missionary Baptist Church. Pastor Robert Burton Porter entered into wedlock with Ms. Edna Hannett, who took an active role at her husband’s side. Reverend Porter was called to Progressive Baptist Church of Los Angeles, CA where he served for 26 years. Pastor Porter went on to his reward on Jaunary 17, 1927. Needless to say, God fixed it to be so that Reverend Arthur Bradford Bowen was the keeper of the flock until he was called to be pastor #2 of Providence.</p>
					<p>During the “learning years,” our church had its growing pains and in the mid 30’s was relocated at a storefront at 12th and Garfield. But by the grace of our Creator, the seed became a little flower at the property at 1302 Tracy, purchase by the members of Providence. This is where the church became a close family. We grew in membership, thrived in spiritualism, became affiliated with national and state conventions and associations.</p>
					<p>In 1959, Providence moved to its present location of 2917 Tracy. In March 1961, Reverend Lewis A. McGhee was called to Providence as our new shepherd. The church mortgage was paid off in 1969. And the church continued to grow. On August 13, 2001, Pastor Lewis McGhee departed and went home to be with the Lord after 40 years of faithful service. We are thankful for our former pastor and his legacy and tremendous blessing. On February 11, 1995, Mark J. McGhee was ordained at Providence and was the associate minister to the pastor.  </p>
					<p>On September 30, 2001, Rev. Mark McGhee was elected to serve as the next Pastor of Providence. Under the leadership of our new pastor, we have a new nursery. The kitchen and sanctuary have been remodeled.</p>
					<p>During the summer of 2011 the front entrance of the church had a makeover done by volunteers from Kansas City Baptist Temple and Louisburg Baptist Church. </p>
					<p>Pastor McGhee’s vision is to establish a Christian school and recreational center for the youth. Also, he wants to have a campus complete with new landscaping, housing for seniors and those who are less fortunate. From now until the Lord returns our mission will be to enrich people’s lives through the word of God, by providing a deeper understanding of His Holy Word, assisting all people to grow in Christ.”</p>
					<p>We’ve came this fair by “FAITH IN GOD” leaning on the Lord, trusting in His Holy Word. And in our 89th year we shall continue in the footsteps of our founder`s and stay close to Christ Jesus our Saviour Reedemer. And our mighty GOD.</p>
